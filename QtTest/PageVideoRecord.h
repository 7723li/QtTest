/*
@brief
录制界面的图像获取子线程
负责从相机缓冲区处获取相机解析好的帧数据
并且 保存视频
*/
class VideoCapture : private QThread
{
public:
	/*
	@note
	父类必须为录制界面
	*/
	VideoCapture(PageVideoRecord* p);
	~VideoCapture(){}

	/*
	@brief
	开始获取图像
	@param[1] c 录制界面当前正在使用的相机 camerabase*
	@param[2] m 录制界面 ->已经分配好<- 的帧空间 cv::Mat*
	@param[3] v 录制界面的VideoWriter cv::VideoWriter*
	*/
	bool begin_capture(
		camerabase* c,
		cv::Mat* m,
		cv::VideoWriter* v);
	/*
	@brief
	停止获取图像
	*/
	void stop_capture();

	/*
	@brief
	尝试开始录制
	*/
	void begin_record();
	/*
	@brief
	尝试停止录制
	*/
	void stop_record();

	/*
	@brief
	图像获取状态(相机是否正在工作)
	@note
	该状态应该必须可以影响到是否可以开始录制
	*/
	bool capturing();
	/*
	@brief
	图像录制状态(是否正在录制)
	@note
	该状态应该必须可以影响到是否可以退出录制界面
	*/
	bool recording();

private:
	virtual void run() override;

private:
	bool need_capture;
	bool need_record;

	camerabase* cam;
	cv::Mat* mat;
	cv::VideoWriter* writer;
};

